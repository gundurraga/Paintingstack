function logImageClick(postId) {
  console.log("Imagen clickeada:", postId);

  const imageUrl = `images/users/gundurraga/download/${postId}.jpg`;

  const modal = document.createElement("div");
  modal.className = "modal";

  modal.innerHTML = `
    <span class="close-button"><ion-icon name="close-outline"></ion-icon></span>
    <div class="modal-content">
      <img src="${imageUrl}" alt="${postId}">
    </div>
  `;

  document.body.appendChild(modal);
  modal.style.display = "block";

  // Disable scrolling on the body
  document.body.style.overflow = "hidden";

  const closeModal = () => {
    modal.style.display = "none";
    modal.remove();
    // Re-enable scrolling on the body
    document.body.style.overflow = "";
  };

  modal.querySelector(".close-button").onclick = closeModal;

  // Modified click event handling
  window.onclick = function (event) {
    if (event.target === modal) {
      closeModal();
    }
  };
}

